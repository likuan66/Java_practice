package demo02;

public class DemoMath {
    public static void main(String[] args) {
        System.out.println(Math.abs(-3.14));//获取绝对值
        System.out.println(Math.ceil(-3.6));//向上取整
        System.out.println(Math.floor(-4.5));//向下取整
        System.out.println(Math.round(4.2563));//四舍五入
    }
}
